#include<stdio.h>


int main() {
	
	char letter;
	
	printf("Please enter a letter: ");
	scanf("%s",&letter);
	
	switch(letter) {
		
		case 'a' : 
		
			printf("'a' is VOWEL!");
			break;
					
		case 'e' : 
		
			printf("'e' is VOWEL!");
			break;
			
		case 'i' : 
		
			printf("'i' is VOWEL!");
			break;
			
		case 'o' : 
		
			printf("'o' is VOWEL!");
			break;
		
		case 'u' : 
		
			printf("'u' is VOWEL!");
			break;
		
		case 'A' : 
		
			printf("'A' is VOWEL!");
			break;
		
		case 'E' : 
		
			printf("'E' is VOWEL!");
			break;
		
		case 'I' : 
		
			printf("'I' is VOWEL!");
			break;
		
		case 'O' : 
		
			printf("'O' is VOWEL!");
			break;
		
		case 'U' : 
		
			printf("'U' is VOWEL!");
			break;
			
		default :
		
			printf("'%c' is CONSONANT!",letter);
			break;	
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
